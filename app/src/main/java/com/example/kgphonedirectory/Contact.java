package com.example.kgphonedirectory;

public class Contact
{

int id;
String name;
String contact;
String email;


void setId(int id)
{
    this.id=id;
}

int getId()
{
    return id;
}

void setName(String name)
{
    this.name = name;
}

String getName()
{
    return name;
}

void setContact(String contact)
{
    this.contact = contact;
}

String getContact()
{
    return contact;
}

void setEmail(String email)
{
    this.email = email;
}

String getEmail()
{
    return email;
}






}
